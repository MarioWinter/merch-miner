# BUG-7 — Avatar stores relative path
# ---------------------------------------------------------------------------

@pytest.mark.django_db
def test_avatar_stores_relative_path(tmp_path, settings):
    """After upload, user.avatar must be a relative /media/... path, not an absolute URL."""
    settings.MEDIA_ROOT = str(tmp_path)
    user = make_user("avatarrel@test.com")
    client = auth_client(user)
    url = reverse("user-avatar")

    img_bytes = _minimal_jpeg_bytes(500)
    img_file = io.BytesIO(img_bytes)
    img_file.name = "avatar.jpg"

    response = client.post(url, {"avatar": img_file}, format="multipart")
    assert response.status_code == 200

    user.refresh_from_db()
    assert user.avatar.startswith("/media/"), (
        f"Expected relative /media/... path, got: {user.avatar}"
    )
    assert not user.avatar.startswith("http"), (
        f"avatar field must not store absolute URL, got: {user.avatar}"
    )
