def make_user(email, password="TestPass123!", active=True, **kwargs):
    return User.objects.create_user(
        email=email,
        password=password,
        username=email,
        is_active=active,
        **kwargs,
    )


def auth_client(user):
    client = APIClient()
    token = str(RefreshToken.for_user(user).access_token)
    client.cookies["access_token"] = token
    return client


def _minimal_jpeg_bytes(size_bytes=100):
    """Return a valid JPEG byte stream generated by Pillow (1x1 white pixel)."""
    from PIL import Image as PilImage
    buf = io.BytesIO()
    img = PilImage.new("RGB", (1, 1), color=(255, 255, 255))
    img.save(buf, format="JPEG")
    return buf.getvalue()


